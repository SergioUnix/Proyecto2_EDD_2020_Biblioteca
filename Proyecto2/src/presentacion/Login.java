    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        /////codigo para cargar el json de Usuarios
        JSONParser parser = new JSONParser();

        try {

            Object obj = parser.parse(new FileReader("usuarios.json"));
            JSONObject jsonObject = (JSONObject) obj;
            System.out.println("JSON LEIDO: " + jsonObject);

            JSONArray array = (JSONArray) jsonObject.get("Usuarios");
            System.out.println("");

            for(int i = 0 ; i < array.size() ; i++) {
                JSONObject jsonObject1 = (JSONObject) array.get(i);

                System.out.println("DATOS DEL USUARIO: " + i);
                System.out.println("Carnet: " + jsonObject1.get("Carnet"));
                System.out.println("Nombre: " + jsonObject1.get("Nombre"));
                System.out.println("Apellido: " + jsonObject1.get("Apellido"));
                System.out.println("Carrera: " + jsonObject1.get("Carrera"));
                System.out.println("Password: " + jsonObject1.get("Password"));
                System.out.println("");

                Usuario username=new Usuario();
                username.setApellido((String) jsonObject1.get("Apellido"));
                String h;
                h =  jsonObject1.get("Carnet").toString();
                username.setCarnet(Integer.parseInt(h));
                username.setCarrera((String) jsonObject1.get("Carrera"));
                username.setNombre((String) jsonObject1.get("Nombre"));
                username.setPassword((String) jsonObject1.get("Password"));

                if(presentacion.Login.listUsuarios.exist(Integer.parseInt(h))==false){
                    listUsuarios.Insertar(username);
                }else{
                    JOptionPane.showMessageDialog(null, "El Usuario Ya existe");
                }

            }

        } catch(FileNotFoundException e) { }
        catch(IOException e) { }
        catch(ParseException e) { }

    }//GEN-LAST:event_jButton1ActionPerformed

